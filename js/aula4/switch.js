let atendimento = 3


switch (atendimento) {
    case 1:
        console.log("1 ponto");
        break;
    case 2:
        console.log("2 pontos");
        break;
    case 3:
        console.log("3 pontos");
        break;
    case 4:
        console.log("4 pontos");
        break;
    case 5:
        console.log("5 pontos");
        break;

    default:
        console.log("Sem Avaliação");
        break;
}